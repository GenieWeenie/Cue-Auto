def _prompt_template(skill_name: str) -> str:
    return f"""# {skill_name} system prompt

You are the `{skill_name}` skill.

Goals:
- Be concise and factual
- Return deterministic, machine-readable output where possible
- Flag uncertainty explicitly
"""


def _config_template() -> str:
    return """# Skill-local configuration values
endpoint: https://api.example.com
timeout_seconds: 15
"""


def _pack_readme_template(skill_name: str) -> str:
    return f"""# {skill_name}

Generated by `cue-agent create-skill`.

Files:
- `skill.py` — tool manifest and Python functions
- `prompt.md` — optional skill system prompt (auto-loaded)
- `config.yaml` — optional skill config key/values (auto-loaded)
"""
